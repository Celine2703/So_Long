#include "so_long_bonus.h"

void	ft_cpt(t_map *map, int l, int c)
{
	if (map ->map[l][c] != '1' && map ->map[l][c] != 'E')
	{
		ft_putnbr_fd(++map ->cpt, 1);
		write(1, "\n", 1);
	}
}
