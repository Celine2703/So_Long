#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	cpt;

	if (!lst)
		return (0);
	cpt = 0;
	while (lst)
	{
		lst = lst->next;
		cpt++;
	}
	return (cpt);
}
