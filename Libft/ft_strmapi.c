#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*tab;

	if (!s)
		return (0);
	i = 0;
	tab = malloc(sizeof(char) * ft_strlen(s) + 1);
	if (tab == 0)
		return (0);
	while (s[i])
	{
		tab[i] = (*f)(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
